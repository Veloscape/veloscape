<div class="help">
    <h3>Using the Veloscape Map</h3>
    <p>
        Share your cycling experiences with us by mapping your journeys on the veloscape map. 
        Place Markers on the map to plot out your routes by either Left Clicking on the map 
        or using the Search Location button. 
        Markers can be dragged around to re-position them if required.
        Keep dropping new Markers until you've mapped out your entire journey!
    </p>
    <div class="img-container">
        <img src="/map/img/map-help-5.png" class="img-responsive">
    </div>
    <p>
        When you're happy with your map, click on the <b>SUBMIT</b> button and give your journey a name 
        (eg: Ashfield to the CBD). Hit <b>CONFIRM</b> to complete your response, its that easy.
    </p>

    <h3>The Marker Controls</h3>
    <p><b>Marker Location</b> - Our 'best guess' of the currently selected Marker's street 
        address. Dont worry if the address is a bit off!
    </p>
    <p><b>Previous Marker</b> - Selects the previous Marker in your journey. </p>
    <p><b>Next Marker</b> - Selects the next Marker in your journey. </p>
    <p><b>Delete Marker</b> - Removes the currently selected Marker from your journey. </p>
        
    <h3>Providing Detailed Feedback</h3>
    <p>
        Completing the addtional feedback questions are entirely optional but much appreciated!
        You may fill in as much, or as little as your like for each Marker.
    </p>

    <p>
        The options in Question 1 can be selected by Left Clicking.
    </p>
    <p>
        The sliders in Question 2 can be dragged left or right, choose the level which fits best.
        Hover over each title (Safety, Momentum and Enjoyment) to see a detailed explanation for the category.
    </p>

    <p><b>Want more help or want to provide feedback? Write to us at veloscape@unsw.edu.au</b></p>

